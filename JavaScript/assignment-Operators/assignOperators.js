// assign operator (=) => assign a value to a variable
var num = 5;
// add and assign operator
// num = num + 5;
num += 5; // 10

// sub and assign operator
// num = num - 2;
num -= 2; // 8

// Multiply and assign
num *= 2; // 16

// Divide and assign
num /= 4; // 4

// Remainder and assign
num %= 3; // 1
