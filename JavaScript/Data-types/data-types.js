// Data types are classified into two types
// 1. Primitive data types
// 2. Non-primitive data types

// Primitive data types
// 1. Number (1, 2, 5.5, -5)
// 2. String ("Hello", 'World')
// 3. Boolean (true, false)
// 4. Undefined (undefined)
// 5. Null (null)

// Non-primitive data types
// 1. Array ([1, 2, 3, 4, 5])
// 2. Object [{a: 1, b: 2}]
// 3. Function (function(){})

var phoneNumber = 1234567890;
var myName = "Anand";
var isIndian = true;
var place;
var laptopPrice = null;

console.log("String", myName);
console.log("Number", phoneNumber);
console.log("Boolean", isIndian);
console.log("Undefined", place);
console.log("Null", laptopPrice);

console.log(
  "For finding the data type we can use typeof method. Example type of myName is a",
  typeof myName
);
